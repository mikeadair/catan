// Shared types for the game engine, Firebase layer, and UI.
// This file is the contract between game/, firebase/, and components/ — keep additions additive.

export type Resource = 'brick' | 'lumber' | 'ore' | 'grain' | 'wool';
export const RESOURCES: Resource[] = ['brick', 'lumber', 'ore', 'grain', 'wool'];

export type ResourceCount = Record<Resource, number>;

// 'gold' produces no fixed resource — rolling it lets each adjacent settlement/city owner
// pick any 1 resource (2 for a city) instead, via the 'pickGoldResources' action/'goldPick'
// phase. Currently only used by the 'fog-of-war' preset.
export type Terrain = 'hills' | 'forest' | 'mountains' | 'fields' | 'pasture' | 'desert' | 'gold';

export const TERRAIN_RESOURCE: Record<Exclude<Terrain, 'desert' | 'gold'>, Resource> = {
  hills: 'brick',
  forest: 'lumber',
  mountains: 'ore',
  fields: 'grain',
  pasture: 'wool',
};

// Axial hex coordinates (pointy-top).
export interface AxialCoord {
  q: number;
  r: number;
}

export interface HexTile {
  id: string; // `${q},${r}`
  coord: AxialCoord;
  terrain: Terrain;
  // null for desert, or (fog-of-war only) for an undiscovered hex whose number hasn't been
  // randomly assigned yet — see 'fog-of-war' handling in rules.ts.
  number: number | null;
}

export type PortType = Resource | 'generic'; // generic = 3:1

export interface Port {
  id: string;
  vertexIds: [string, string]; // the two vertices that can use this port
  type: PortType;
}

// Vertices and edges are identified by a canonical string id derived from the
// (sorted) hex corners that touch them, generated by board.ts. UI/game code
// should treat these ids as opaque.
export type VertexId = string;
export type EdgeId = string;

export interface VertexInfo {
  id: VertexId;
  // pixel-independent position used for rendering: cube corner shared by up to 3 hexes
  adjacentHexIds: string[];
  adjacentVertexIds: VertexId[];
  adjacentEdgeIds: EdgeId[];
}

export interface EdgeInfo {
  id: EdgeId;
  vertexIds: [VertexId, VertexId];
  adjacentHexIds: string[];
}

export interface Board {
  hexes: HexTile[];
  vertices: Record<VertexId, VertexInfo>;
  edges: Record<EdgeId, EdgeInfo>;
  ports: Port[];
  robberHexId: string;
}

export type MapPresetId = 'official-beginner' | 'balanced-random' | 'chaos' | 'extended-5-6p' | 'fog-of-war';

export interface MapPreset {
  id: MapPresetId;
  name: string;
  description: string;
}

export type BuildingType = 'settlement' | 'city';

export interface Building {
  type: BuildingType;
  uid: string;
}

export type DevCardType = 'knight' | 'roadBuilding' | 'yearOfPlenty' | 'monopoly' | 'victoryPoint';

export interface DevCard {
  id: string;
  type: DevCardType;
  boughtTurn: number;
}

export type GamePhase =
  | 'lobby'
  | 'setup1'
  | 'setup2'
  | 'roll'
  | 'main'
  | 'discard'
  | 'robber'
  // One or more players rolled a gold hex's number and must each pick their resource(s)
  // before play resumes — see pendingGoldPicks on RoomState.
  | 'goldPick'
  | 'gameOver';

export const PLAYER_COLORS = ['red', 'blue', 'white', 'orange', 'green', 'brown'] as const;
export type PlayerColor = (typeof PLAYER_COLORS)[number];

export type BotDifficulty = 'normal';

// --- Public, room-readable player summary (no hidden info) ---
export interface PublicPlayer {
  uid: string;
  displayName: string;
  color: PlayerColor;
  isBot: boolean;
  botDifficulty?: BotDifficulty;
  seatIndex: number;
  resourceCount: number;
  devCardCount: number;
  visibleVictoryPoints: number; // excludes hidden VP dev cards
  knightsPlayed: number;
  roadsBuilt: number;
  settlementsBuilt: number;
  citiesBuilt: number;
  connected: boolean;
  lastSeen: number; // ms epoch, client-set
}

// --- Private per-player hand, readable only by that player ---
export interface PrivateHand {
  resources: ResourceCount;
  devCards: DevCard[];
}

export interface LogEntry {
  id: string;
  ts: number;
  message: string;
}

export type TradeStatus = 'pending' | 'accepted' | 'rejected' | 'cancelled' | 'countered';

export interface TradeOffer {
  id: string;
  proposerUid: string;
  targetUid: string | null; // null = open to all
  give: Partial<ResourceCount>;
  receive: Partial<ResourceCount>;
  status: TradeStatus;
  counterOf: string | null;
  createdAt: number;
  // Open trades (targetUid === null) only: uids who've accepted and are waiting for the
  // proposer to pick one via finalizeTrade. Accepting a *targeted* trade still executes
  // immediately (see respondTrade in rules.ts) since there's only ever one possible
  // responder, so this stays empty for those. Optional so older-shaped docs still typecheck.
  interestedUids?: string[];
}

// --- Room / shared game state document ---
export interface RoomState {
  id: string;
  code: string;
  hostUid: string;
  status: 'lobby' | 'playing' | 'finished';
  mapPreset: MapPresetId;
  // Board layout is a deterministic function of this seed, so it's safe public info. Once a
  // game has started, though, the same seed is also the sole input to the dev-card shuffle
  // (see devCardDeck below) — leaking it after that point is equivalent to leaking the deck
  // order. The startGame Cloud Function generates a fresh seed and never writes the real
  // value to this public field once status is 'playing'; only the pre-game lobby placeholder
  // is meaningful here.
  seed: string;
  board: Board | null; // null until game starts
  vertices: Record<VertexId, Building>; // sparse
  edges: Record<EdgeId, string>; // edgeId -> owner uid, sparse
  turnOrder: string[]; // uids in seat order
  currentPlayerIndex: number;
  phase: GamePhase;
  diceRoll: [number, number] | null;
  bank: ResourceCount;
  devCardDeck: DevCardType[]; // remaining, shuffled. Server-only: the Firestore adapter keeps this out of the public doc (see serverOnly/devDeck) and mirrors its length via devCardDeckCount below.
  devCardDeckCount: number; // public, client-safe mirror of devCardDeck.length
  longestRoadUid: string | null;
  largestArmyUid: string | null;
  winnerUid: string | null;
  turnNumber: number;
  turnStartedAt: number;
  setupRound: 1 | 2 | null;
  pendingDiscardUids: string[]; // players who must discard on a 7
  botActionClaim: { turnNumber: number; ts: number } | null;
  log: LogEntry[];
  createdAt: number;
  // Host-configurable house rules, set at room creation, fixed once status leaves 'lobby'.
  victoryPointsToWin: number; // default DEFAULT_VICTORY_POINTS_TO_WIN
  discardLimit: number; // hand size that triggers a discard on a rolled 7; default DEFAULT_DISCARD_LIMIT
  turnTimerSeconds: number | null; // countdown shown per turn; null = disabled. Enforced server-side via 'timeoutEndTurn'.
  // Non-bot majority vote to freeze/unfreeze the game — see voteToPause/voteToUnpause in
  // rules.ts. While paused, the turn timer and AFK auto-roll both stop counting elapsed time.
  paused: boolean;
  pausedAt: number | null; // Date.now() when `paused` flipped true; used to shift turnStartedAt forward by the pause duration on unpause, so elapsed-time math resumes where it left off.
  pauseVotes: string[]; // uids currently voting to flip `paused` the opposite way of its current value
  // fog-of-war only (null on every other preset): hex ids whose terrain is revealed to
  // players and which produce resources on a roll. Starts as the board's corner hexes, the
  // desert, and the gold hex (see board.ts); a road reaching a new hex reveals it — see
  // 'buildRoad' in rules.ts.
  discoveredHexIds: string[] | null;
  // Players who rolled a gold hex's number this turn and still owe a resource pick (1 per
  // settlement, 2 per city touching it) — see 'pickGoldResources' and the 'goldPick' phase.
  pendingGoldPicks: { uid: string; amount: number }[];
  // Added by game/rules.ts (additive, optional so other constructors aren't broken):
  devCardPlayedThisTurn?: boolean; // at most one dev card may be played per turn
  lastSetupSettlementVertexId?: VertexId | null; // anchors the free setup road to the settlement just placed
}

