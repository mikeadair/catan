// Shared types for the game engine, Firebase layer, and UI.
// This file is the contract between game/, firebase/, and components/ — keep additions additive.

export type Resource = 'brick' | 'lumber' | 'ore' | 'grain' | 'wool';
export const RESOURCES: Resource[] = ['brick', 'lumber', 'ore', 'grain', 'wool'];

export type ResourceCount = Record<Resource, number>;

// 'gold' produces no fixed resource — rolling it lets each adjacent settlement/city owner
// pick any 1 resource (2 for a city) instead, via the 'pickGoldResources' action/'goldPick'
// phase. Currently only used by the 'fog-of-war' preset.
export type Terrain = 'hills' | 'forest' | 'mountains' | 'fields' | 'pasture' | 'desert' | 'gold';

export const TERRAIN_RESOURCE: Record<Exclude<Terrain, 'desert' | 'gold'>, Resource> = {
  hills: 'brick',
  forest: 'lumber',
  mountains: 'ore',
  fields: 'grain',
  pasture: 'wool',
};

// Axial hex coordinates (pointy-top).
export interface AxialCoord {
  q: number;
  r: number;
}

export interface HexTile {
  id: string; // `${q},${r}`
  coord: AxialCoord;
  terrain: Terrain;
  // null for desert, or (fog-of-war only) for an undiscovered hex whose number hasn't been
  // randomly assigned yet — see 'fog-of-war' handling in rules.ts.
  number: number | null;
}

export type PortType = Resource | 'generic'; // generic = 3:1

export interface Port {
  id: string;
  vertexIds: [string, string]; // the two vertices that can use this port
  type: PortType;
}

// Vertices and edges are identified by a canonical string id derived from the
// (sorted) hex corners that touch them, generated by board.ts. UI/game code
// should treat these ids as opaque.
export type VertexId = string;
export type EdgeId = string;

export interface VertexInfo {
  id: VertexId;
  // pixel-independent position used for rendering: cube corner shared by up to 3 hexes
  adjacentHexIds: string[];
  adjacentVertexIds: VertexId[];
  adjacentEdgeIds: EdgeId[];
}

export interface EdgeInfo {
  id: EdgeId;
  vertexIds: [VertexId, VertexId];
  adjacentHexIds: string[];
}

export interface Board {
  hexes: HexTile[];
  vertices: Record<VertexId, VertexInfo>;
  edges: Record<EdgeId, EdgeInfo>;
  ports: Port[];
  robberHexId: string;
}

export type MapPresetId = 'official-beginner' | 'balanced-random' | 'chaos' | 'extended-5-6p' | 'fog-of-war';

export interface MapPreset {
  id: MapPresetId;
  name: string;
  // One or two short sentences — rendered directly on the picker card, so it has to fit the
  // same footprint as every other card's blurb.
  description: string;
  // Optional full rules explanation for presets whose mechanics need more than the blurb
  // (surfaced as a tooltip/expandable, never inline on the card).
  details?: string;
}

export type BuildingType = 'settlement' | 'city';

export interface Building {
  type: BuildingType;
  uid: string;
}

export type DevCardType = 'knight' | 'roadBuilding' | 'yearOfPlenty' | 'monopoly' | 'victoryPoint';

export interface DevCard {
  id: string;
  type: DevCardType;
  boughtTurn: number;
}

// Wire values are frozen (they live in every existing Firestore room doc and in
// firestore.rules string literals) — only the symbolic names may ever change. Referencing
// these as GamePhase.Main / RoomStatus.Playing instead of bare strings makes call sites
// greppable ('main' alone collides with the git branch, CSS, phase arrays, ...).
export const GamePhase = {
  Lobby: 'lobby',
  Setup1: 'setup1',
  Setup2: 'setup2',
  Roll: 'roll',
  Main: 'main',
  Discard: 'discard',
  Robber: 'robber',
  // One or more players rolled a gold hex's number and must each pick their resource(s)
  // before play resumes — see pendingGoldPicks on RoomState.
  GoldPick: 'goldPick',
  GameOver: 'gameOver',
} as const;
export type GamePhase = (typeof GamePhase)[keyof typeof GamePhase];

export const RoomStatus = {
  Lobby: 'lobby',
  Playing: 'playing',
  Finished: 'finished',
} as const;
export type RoomStatus = (typeof RoomStatus)[keyof typeof RoomStatus];

export const PLAYER_COLORS = ['red', 'blue', 'white', 'orange', 'green', 'brown'] as const;
export type PlayerColor = (typeof PLAYER_COLORS)[number];

export type BotDifficulty = 'easy' | 'normal' | 'hard';

