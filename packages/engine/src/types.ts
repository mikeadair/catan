// --- Game actions: the single vocabulary of legal moves, applied via game/rules.ts ---
export type GameAction =
  | { type: 'rollDice'; uid: string }
  | { type: 'buildRoad'; uid: string; edgeId: EdgeId; free?: boolean }
  | { type: 'buildSettlement'; uid: string; vertexId: VertexId; free?: boolean }
  | { type: 'buildCity'; uid: string; vertexId: VertexId }
  | { type: 'buyDevCard'; uid: string }
  | { type: 'playKnight'; uid: string; devCardId: string; robberHexId: string; stealFromUid: string | null }
  // Arms room.pendingRoadBuilding — the two free roads are then placed one at a time via
  // ordinary buildRoad actions, so each placement lands (and reveals fog) immediately.
  | { type: 'playRoadBuilding'; uid: string; devCardId: string }
  | { type: 'playYearOfPlenty'; uid: string; devCardId: string; resources: [Resource, Resource] }
  | { type: 'playMonopoly'; uid: string; devCardId: string; resource: Resource }
  | { type: 'moveRobber'; uid: string; robberHexId: string; stealFromUid: string | null } // post-7 roll, no card
  | { type: 'discard'; uid: string; resources: Partial<ResourceCount> }
  // Any room member may submit this once DISCARD_TIMEOUT_SECONDS has actually elapsed since
  // room.discardPhaseStartedAt (re-validated server-side, never trusted from the client) —
  // randomly discards down to the required count for every uid still in pendingDiscardUids,
  // not just one, since a 7 roll can leave several players owing a discard simultaneously.
  | { type: 'timeoutDiscard'; uid: string }
  // Any room member may submit this once ROBBER_TIMEOUT_SECONDS has actually elapsed since
  // room.robberPhaseStartedAt — moves the robber to a random legal hex on the current
  // player's behalf (no steal) so an AFK/stuck current player doesn't stall the game.
  | { type: 'timeoutRobber'; uid: string }
  // Any room member may submit this once SETUP_TIMEOUT_SECONDS has actually elapsed since
  // room.setupTurnStartedAt — auto-places a random legal settlement or road (whichever the
  // current player still owes for this setup turn) on their behalf, same "don't stall the
  // game" rationale as timeoutRobber/timeoutDiscard.
  | { type: 'timeoutSetupPlacement'; uid: string }
  // fog-of-war only: pays out a pending gold-hex pick (see pendingGoldPicks/'goldPick' phase).
  // resources.length must exactly match the owed amount; entries need not be distinct.
  | { type: 'pickGoldResources'; uid: string; resources: Resource[] }
  | { type: 'bankTrade'; uid: string; give: Resource; giveAmount: number; receive: Resource }
  | { type: 'proposeTrade'; uid: string; give: Partial<ResourceCount>; receive: Partial<ResourceCount>; targetUid: string | null }
  | { type: 'respondTrade'; uid: string; tradeId: string; accept: boolean }
  // A responder's counter-proposal to a pending trade: rejects the original (status
  // 'countered') and creates a new offer targeted back at the original proposer. Exists as
  // its own action (rather than respondTrade + proposeTrade) because proposeTrade requires
  // the current player, and a responder by definition isn't.
  | { type: 'counterTrade'; uid: string; tradeId: string; give: Partial<ResourceCount>; receive: Partial<ResourceCount> }
  | { type: 'cancelTrade'; uid: string; tradeId: string }
  // Proposer-only: picks one of an open trade's interestedUids to actually execute the
  // swap with. Targeted trades never need this — accepting one executes immediately via
  // respondTrade, since there's only ever one possible responder.
  | { type: 'finalizeTrade'; uid: string; tradeId: string; withUid: string }
  | { type: 'endTurn'; uid: string }
  // uid === targetUid is a self-leave; otherwise uid must be the host removing a bot seat.
  // Only legal outside setup1/setup2 (see rules.ts) — the snake-ordered setup turn sequence
  // doesn't generalize cleanly to a mutating turnOrder mid-snake.
  | { type: 'removeSeat'; uid: string; targetUid: string }
  // Any room member may submit this once the current turn's clock has actually run out
  // server-side (re-validated in rules.ts, never trusted from the client) — advances the
  // turn exactly like endTurn, crediting the timeout to whoever was current, not the caller.
  | { type: 'timeoutEndTurn'; uid: string }
  // Any room member may submit this at any time; rules.ts re-validates that at least one
  // pending trade has actually aged past TRADE_EXPIRY_MS server-side (never trusted from the
  // client) and flips only those to 'expired'. Mirrors timeoutEndTurn's "any connected client
  // may report it" pattern — see web/src/state/store.ts for the polling that drives this.
  | { type: 'expireTrades'; uid: string }
  // Any room member may submit this at any time; rules.ts re-validates that at least one
  // pending trade has a responder who hasn't answered within room.tradeResponseTimerSeconds
  // of the trade's createdAt (never trusted from the client), and auto-rejects every such
  // responder on that trade (not the whole trade, unlike expireTrades) — see
  // pendingTradeResponders in rules.ts and web/src/state/store.ts for the polling.
  | { type: 'timeoutTradeResponse'; uid: string }
  // uid casts (or withdraws) a vote to flip `paused` the opposite way of its current value.
  // Bots never vote; majority is computed over non-bot turnOrder members only.
  | { type: 'voteToPause'; uid: string }
  | { type: 'voteToUnpause'; uid: string };
