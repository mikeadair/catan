// --- Game actions: the single vocabulary of legal moves, applied via game/rules.ts ---
export type GameAction =
  | { type: 'rollDice'; uid: string }
  | { type: 'buildRoad'; uid: string; edgeId: EdgeId; free?: boolean }
  | { type: 'buildSettlement'; uid: string; vertexId: VertexId; free?: boolean }
  | { type: 'buildCity'; uid: string; vertexId: VertexId }
  | { type: 'buyDevCard'; uid: string }
  | { type: 'playKnight'; uid: string; devCardId: string; robberHexId: string; stealFromUid: string | null }
  | { type: 'playRoadBuilding'; uid: string; devCardId: string; edgeIds: [EdgeId, EdgeId] }
  | { type: 'playYearOfPlenty'; uid: string; devCardId: string; resources: [Resource, Resource] }
  | { type: 'playMonopoly'; uid: string; devCardId: string; resource: Resource }
  | { type: 'moveRobber'; uid: string; robberHexId: string; stealFromUid: string | null } // post-7 roll, no card
  | { type: 'discard'; uid: string; resources: Partial<ResourceCount> }
  // fog-of-war only: pays out a pending gold-hex pick (see pendingGoldPicks/'goldPick' phase).
  // resources.length must exactly match the owed amount; entries need not be distinct.
  | { type: 'pickGoldResources'; uid: string; resources: Resource[] }
  | { type: 'bankTrade'; uid: string; give: Resource; giveAmount: number; receive: Resource }
  | { type: 'proposeTrade'; uid: string; give: Partial<ResourceCount>; receive: Partial<ResourceCount>; targetUid: string | null }
  | { type: 'respondTrade'; uid: string; tradeId: string; accept: boolean }
  | { type: 'cancelTrade'; uid: string; tradeId: string }
  // Proposer-only: picks one of an open trade's interestedUids to actually execute the
  // swap with. Targeted trades never need this — accepting one executes immediately via
  // respondTrade, since there's only ever one possible responder.
  | { type: 'finalizeTrade'; uid: string; tradeId: string; withUid: string }
  | { type: 'endTurn'; uid: string }
  // uid === targetUid is a self-leave; otherwise uid must be the host removing a bot seat.
  // Only legal outside setup1/setup2 (see rules.ts) — the snake-ordered setup turn sequence
  // doesn't generalize cleanly to a mutating turnOrder mid-snake.
  | { type: 'removeSeat'; uid: string; targetUid: string }
  // Any room member may submit this once the current turn's clock has actually run out
  // server-side (re-validated in rules.ts, never trusted from the client) — advances the
  // turn exactly like endTurn, crediting the timeout to whoever was current, not the caller.
  | { type: 'timeoutEndTurn'; uid: string }
  // uid casts (or withdraws) a vote to flip `paused` the opposite way of its current value.
  // Bots never vote; majority is computed over non-bot turnOrder members only.
  | { type: 'voteToPause'; uid: string }
  | { type: 'voteToUnpause'; uid: string };
