export interface LogEntry {
  id: string;
  ts: number;
  message: string;
  // Optional structured payload so the client can render small inline graphics (dice pips,
  // mini resource-card icons) instead of parsing `message` text — fragile, and `message` is
  // already generated server-side, so it's cheaper for the server to also emit structured
  // data for the entry kinds worth illustrating. `message` remains the source of truth /
  // fallback: entry kinds that don't bother with `meta`, and any log entries persisted
  // before this field existed, are `meta`-less and must keep rendering fine from `message`
  // alone (no migration needed).
  //
  // Only ever carries information that's already public in the physical game (dice rolls,
  // bank/player trades, roll/Monopoly/Year-of-Plenty resource gains) — never anything meant
  // to stay hidden, like which specific resource a robber steal yielded.
  meta?: LogEntryMeta;
}

export type LogEntryMeta =
  | { kind: 'diceRoll'; roll: [number, number]; gains?: Record<string, Partial<ResourceCount>> }
  | { kind: 'resourceGain'; uid: string; resources: Partial<ResourceCount> }
  // toUid: null = traded with the bank (no counterparty player).
  | { kind: 'resourceTrade'; fromUid: string; toUid: string | null; give: Partial<ResourceCount>; receive: Partial<ResourceCount> };

export type TradeStatus = 'pending' | 'accepted' | 'rejected' | 'cancelled' | 'countered' | 'expired';

// How long a trade offer may sit 'pending' before it's eligible for server-side expiry (see
// the 'expireTrades' action in rules.ts). Generous enough that human players have time to
// notice and respond, but bounded so an open offer with no interest doesn't sit forever —
// there's otherwise no other TTL on a trade beyond the proposer's own next endTurn/timeout.
export const TRADE_EXPIRY_MS = 90_000;

// Floor on the above: an open (targetUid === null) trade must remain answerable for at least
// this long, independent of how quickly bots resolve any *targeted* trade aimed at them (see
// BOT_TRADE_RESPONSE_DELAY_*_MS in web/src/state/store.ts, which can fire in as little as
// ~1s). A bot settling its own targeted trade fast must never shrink the window other human
// players get to act on a separate open offer. Asserted below rather than just documented, so
// a future change to TRADE_EXPIRY_MS can't silently violate it.
export const MIN_OPEN_TRADE_WINDOW_MS = 15_000;
if (TRADE_EXPIRY_MS < MIN_OPEN_TRADE_WINDOW_MS) {
  throw new Error('TRADE_EXPIRY_MS must be at least MIN_OPEN_TRADE_WINDOW_MS');
}

export interface TradeOffer {
  id: string;
  proposerUid: string;
  targetUid: string | null; // null = open to all
  give: Partial<ResourceCount>;
  receive: Partial<ResourceCount>;
  status: TradeStatus;
  counterOf: string | null;
  createdAt: number;
  // room.turnNumber when this offer was proposed. Lets bots apply a cross-turn cooldown
  // before re-asking for the same resources after a rejection (see askedRecently in bots.ts),
  // instead of re-proposing every single turn while a deficit persists. Optional so
  // older-shaped docs still typecheck.
  proposedTurn?: number;
  // Open trades (targetUid === null) only: uids who've accepted and are waiting for the
  // proposer to pick one via finalizeTrade. Accepting a *targeted* trade still executes
  // immediately (see respondTrade in rules.ts) since there's only ever one possible
  // responder, so this stays empty for those. Optional so older-shaped docs still typecheck.
  interestedUids?: string[];
  // Open trades (targetUid === null) only: uids who've explicitly rejected this offer.
  // Mirrors interestedUids so clients can render a per-responder accepted/rejected status
  // (see TradeOffers.tsx) for open trades, not just targeted ones (where a single responder's
  // rejection is already captured by `status` flipping to 'rejected'). A uid moves back out of
  // here if they later accept. Optional so older-shaped docs still typecheck.
  rejectedUids?: string[];
}

// --- Room / shared game state document ---
export interface RoomState {
  id: string;
  code: string;
  hostUid: string;
  status: RoomStatus;
  mapPreset: MapPresetId;
  // Board layout is a deterministic function of this seed, so it's safe public info. Once a
  // game has started, though, the same seed is also the sole input to the dev-card shuffle
  // (see devCardDeck below) — leaking it after that point is equivalent to leaking the deck
  // order. The startGame Cloud Function generates a fresh seed and never writes the real
  // value to this public field once status is 'playing'; only the pre-game lobby placeholder
  // is meaningful here.
  seed: string;
  board: Board | null; // null until game starts
  vertices: Record<VertexId, Building>; // sparse
  edges: Record<EdgeId, string>; // edgeId -> owner uid, sparse
  turnOrder: string[]; // uids in seat order
  currentPlayerIndex: number;
  phase: GamePhase;
  diceRoll: [number, number] | null;
  bank: ResourceCount;
  devCardDeck: DevCardType[]; // remaining, shuffled. Server-only: the Firestore adapter keeps this out of the public doc (see serverOnly/devDeck) and mirrors its length via devCardDeckCount below.
  devCardDeckCount: number; // public, client-safe mirror of devCardDeck.length
  longestRoadUid: string | null;
  largestArmyUid: string | null;
  winnerUid: string | null;
  turnNumber: number;
  turnStartedAt: number;
  setupRound: 1 | 2 | null;
  pendingDiscardUids: string[]; // players who must discard on a 7
  // Date.now() when the room entered the 'discard' phase; null whenever it isn't. Drives the
  // fixed DISCARD_TIMEOUT_SECONDS countdown (see 'timeoutDiscard' in rules.ts) shared by every
  // pending discarder, unlike the per-turn timer which only ever concerns one player at a time.
  discardPhaseStartedAt: number | null;
  // Date.now() when the room entered the 'robber' phase; null whenever it isn't. Drives the
  // fixed ROBBER_TIMEOUT_SECONDS countdown (see 'timeoutRobber' in rules.ts) so a current
  // player who goes AFK/offline while deciding where to place the robber doesn't stall the
  // game indefinitely — mirrors discardPhaseStartedAt above.
  robberPhaseStartedAt: number | null;
  // Date.now() when the *current* player's setup turn began — reset on every advance (a new
  // current player, or the settlement->road hand-off within one player's own setup turn), null
  // once setup phase ends. Drives the fixed SETUP_TIMEOUT_SECONDS countdown (see
  // 'timeoutSetupPlacement' in rules.ts) — mirrors discardPhaseStartedAt/robberPhaseStartedAt
  // above, just per-player-turn instead of shared/single-phase-wide.
  setupTurnStartedAt: number | null;
  botActionClaim: { turnNumber: number; ts: number } | null;
  log: LogEntry[];
  createdAt: number;
  // Host-configurable house rules, set at room creation, fixed once status leaves 'lobby'.
  victoryPointsToWin: number; // default DEFAULT_VICTORY_POINTS_TO_WIN
  discardLimit: number; // hand size that triggers a discard on a rolled 7; default DEFAULT_DISCARD_LIMIT
  turnTimerSeconds: number | null; // countdown shown per turn; null = disabled. Enforced server-side via 'timeoutEndTurn'.
  // How long a responder has to answer a pending trade before they're auto-rejected on its
  // behalf; null = disabled. Default DEFAULT_TRADE_RESPONSE_TIMER_SECONDS. Enforced
  // server-side via 'timeoutTradeResponse' — distinct from TRADE_EXPIRY_MS, which is a fixed,
  // non-configurable ceiling on the whole trade's lifetime regardless of individual responses.
  tradeResponseTimerSeconds: number | null;
  // When on, the robber can't target a hex touching any player's settlement/city while that
  // player has fewer than 3 visible victory points — see hexProtectsWeakPlayer in rules.ts
  // ('moveRobber'/'playKnight'). Fails open rather than leaving the robber with no legal
  // target if every hex happens to be protected.
  safeMode: boolean;
  // Non-bot majority vote to freeze/unfreeze the game — see voteToPause/voteToUnpause in
  // rules.ts. While paused, the turn timer and AFK auto-roll both stop counting elapsed time.
  paused: boolean;
  pausedAt: number | null; // Date.now() when `paused` flipped true; used to shift turnStartedAt forward by the pause duration on unpause, so elapsed-time math resumes where it left off.
  pauseVotes: string[]; // uids currently voting to flip `paused` the opposite way of its current value
  // fog-of-war only (null on every other preset): hex ids whose terrain is revealed to
  // players and which produce resources on a roll. Starts as the board's corner hexes, the
  // desert, and the gold hex (see board.ts); a road reaching a new hex reveals it — see
  // 'buildRoad' in rules.ts.
  discoveredHexIds: string[] | null;
  // Players who rolled a gold hex's number this turn and still owe a resource pick (1 per
  // settlement, 2 per city touching it) — see 'pickGoldResources' and the 'goldPick' phase.
  pendingGoldPicks: { uid: string; amount: number }[];
  // Added by game/rules.ts (additive, optional so other constructors aren't broken):
  devCardPlayedThisTurn?: boolean; // at most one dev card may be played per turn
  // Set when the current player plays a Road Building card; each free road then goes through
  // the ordinary buildRoad action (placing immediately, revealing fog hexes, etc.) while this
  // counts down. Cleared when it hits zero or the turn ends. Optional so older room docs
  // (which never had the field) still typecheck; absent reads as "none pending".
  pendingRoadBuilding?: { uid: string; roadsRemaining: number } | null;
  lastSetupSettlementVertexId?: VertexId | null; // anchors the free setup road to the settlement just placed
  // Total extension (ms) already granted to the current turn via proposeTrade (see
  // extendTurnTimerForTrade in rules.ts) — tracked so repeated trade proposals in the same
  // turn can't push the effective deadline past TURN_TIMER_EXTENSION_CAP_MULTIPLIER x the
  // room's initial turnTimerSeconds. Reset to 0 whenever turnStartedAt resets for a new turn.
  turnTimerExtensionMs?: number;
}

