// --- Game actions: the single vocabulary of legal moves, applied via game/rules.ts ---
export type GameAction =
  | { type: 'rollDice'; uid: string }
  | { type: 'buildRoad'; uid: string; edgeId: EdgeId; free?: boolean }
  | { type: 'buildSettlement'; uid: string; vertexId: VertexId; free?: boolean }
  | { type: 'buildCity'; uid: string; vertexId: VertexId }
  | { type: 'buyDevCard'; uid: string }
  | { type: 'playKnight'; uid: string; devCardId: string; robberHexId: string; stealFromUid: string | null }
  | { type: 'playRoadBuilding'; uid: string; devCardId: string; edgeIds: [EdgeId, EdgeId] }
  | { type: 'playYearOfPlenty'; uid: string; devCardId: string; resources: [Resource, Resource] }
  | { type: 'playMonopoly'; uid: string; devCardId: string; resource: Resource }
  | { type: 'moveRobber'; uid: string; robberHexId: string; stealFromUid: string | null } // post-7 roll, no card
  | { type: 'discard'; uid: string; resources: Partial<ResourceCount> }
  | { type: 'bankTrade'; uid: string; give: Resource; giveAmount: number; receive: Resource }
  | { type: 'proposeTrade'; uid: string; give: Partial<ResourceCount>; receive: Partial<ResourceCount>; targetUid: string | null }
  | { type: 'respondTrade'; uid: string; tradeId: string; accept: boolean }
  | { type: 'cancelTrade'; uid: string; tradeId: string }
  | { type: 'endTurn'; uid: string };
